public class ThreeSum {


    public List<List<Integer>> threeSum(int[] nums) {
        List<List<Integer>> list = new ArrayList<List<Integer>>();
        if (nums == null || nums.length < 3) {
            return list;
        }

        Arrays.sort(nums);



        return list;
    }

}
